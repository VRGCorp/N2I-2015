Pour se protéger d'un tremblement de terre, la meilleure réaction est de se réfugier sous une table pour éviter d'être blessé par des chutes de débris.
En effet, le plus gros risque est qu'un bâtiment s'effondre ou qu'un objet lourd tombe.

Au Japon il y a environ 300 séismes par an. Les bâtiments sont donc construit sur les bases en caoutchouc pour résister aux vibrations et ne pas s'effondrer.

Enfin, les tremblements de terre sont rarements très destructeurs, il ne faut donc rester calme et sortir des bâtiments sans précipitation.