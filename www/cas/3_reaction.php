<p>Le danger est l'air contaminé, s'il est dehors il faut se réfugier d'urgence dans un bâtiment.<br/>
Une fois à l'intérieur il est très important de fermer les fenêtres et les portes et d'empêcher l'air d'entrer dans l'abri.<br/>
Si l'air toxique est à l'intérieur il faut vite sortir du bâtiment et respirer le moins possible.<br/>
Une fois en sécurité appeler les secours (le 18 ou le 112).</p>