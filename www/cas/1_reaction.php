<p>En cas d'incendie il est très important de prévenir tout le monde et d'appeler les pompiers (téléphoner au 18 ou au 112).<br/>
Si l'on se trouve dans un bâtiment et qu'un incendie se déclare il faut garder son calme et se diriger vers la sortie la plus proche.<br/>
Se baisser est aussi essentiel car les fumées toxiques montent vers le plafond, l'air est donc plus respirable près du sol.<br/>
Une fois à l'extérieur il faut se réunir en sécurité et attendre les secours.<br/>
